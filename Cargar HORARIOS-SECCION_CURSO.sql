DECLARE @sec_id INT = 1
DECLARE @contador INT = 0
DECLARE @prof_id INT = 1
WHILE @sec_id <= 18
BEGIN
	SET @contador = @contador+1
        INSERT INTO SECCION_CURSO ([sec_id], [cur_id],[prof_id]) VALUES 
            (@sec_id, @contador, @prof_id),
			(@sec_id+1, @contador, @prof_id+1),
			(@sec_id+1, @contador, @prof_id+1)
			IF @contador%9=0
	BEGIN
		SET @prof_id = @prof_id+1
		SET @sec_id = @sec_id+1
		SET @contador = 0
	END	
END

